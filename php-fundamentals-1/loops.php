	<?php
		/* For Loop
		======================================*/

		// Отпечатване на числата от едно до сто.
		// $i = 1 - Инициализираме променлива, която казва от кога ще започне да върти цикълът.
		// Поставяме условие, до кога ще се върти/изпълнява цикълът $i <= 100;.
		// Инкрементираме променливата с единица за да се изпълни условието за край на цикъла $i++.
		for ($i = 1; $i <= 100; $i++) { 
			// В тялото на цикъла поставяме код за изпълнение echo i."<br />";. 
			/* На всяка интерация кръг от цикъла ще печатим стойноста на i, 
			като на първия кръг ще е едно, на втория две и тн. i променя стойноста си при инкремент.
			Инкремент на i се извършва като последна операция, първо изеършва всички други операции. */
			echo i."<br />";
		}
	?>
